package kr.kosta.gathering.alarm.domain;

public class IdentifiedNotice {

	private String email;
	private int nid;
	private int gid;
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public int getNid() {
		return nid;
	}
	public void setNid(int nid) {
		this.nid = nid;
	}
	public int getGid() {
		return gid;
	}
	public void setGid(int gid) {
		this.gid = gid;
	}
	
	@Override
	public String toString() {
		return "IdentifiedNotice [email=" + email + ", nid=" + nid + ", gid="
				+ gid + "]";
	}
}
